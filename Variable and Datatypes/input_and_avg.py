# Question:- Write a python program to find average of two numbers entered by the user.

a = int(input("Enter your first number = ")) # int is used to convert the user input to int. we can also write float to convert it into float data type
b = int(input("Enter your second number = ")) # int is used to convert the user input to int. we can also write float to convert it into float data type
c = (a + b) / 2 # mathematical calculation to find the average of 2 numbers
print("The average of the 2 numbers is:- ", c) # printing the average of the 2 numbers